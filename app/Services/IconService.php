    {
        $sizeClass = "w-{$size} h-{$size}";
        $strokeWidth = $type === 'outline' ? '1.5' : '2';

        // Common SVG attributes
        $attributes = [
            'class' => $sizeClass,
            'fill' => $type === 'solid' ? 'currentColor' : 'none',
            'stroke' => $type === 'outline' ? 'currentColor' : 'none',
            'viewBox' => '0 0 24 24',
            'stroke-width' => $type === 'outline' ? $strokeWidth : '0',
            'stroke-linecap' => 'round',
            'stroke-linejoin' => 'round',
            'aria-hidden' => 'true',
        ];

        $svgAttributes = '';
        foreach ($attributes as $key => $value) {
            if ($value !== null) {
                $svgAttributes .= " {$key}=\"{$value}\"";
            }
        }

        // Icon path definitions
        $paths = self::getIconPaths($name, $type);

        return "<svg{$svgAttributes}>{$paths}</svg>";
    }

